namespace PhpStep\Tokens;

/**
 * An instance of Test Token
 * This represents a simple text cell, without any field tags
 * @author girish
 */
class TextToken extends AbstractToken {
    /** Returns the cellText
     * @return string */
    public function text() : string {
        return $this->text();
    }
}
